// draw image
void draw_rle_image(RLEPixel *image, int size, int start_x, int start_y) {
    int x_pos = start_x;
    int y_pos = start_y;
    for (int i = 0; i < size; i++) {
        for (int j = 0; j < image[i].length; j++) {
            set_pixel(x_pos, y_pos, image[i].color);
            x_pos++;
            if (x_pos >= rle_image_width) {  
                x_pos = start_x;
                y_pos++;
            }
        }
    }
}






